public class Address
{
    private String addr;
    private Integer door;

    public String getAddr()
    {
        return addr;
    }

    public void setAddr(String addr)
    {
        this.addr = addr;
    }

    public Integer getDoor()
    {
        return door;
    }

    public void setDoor(Integer door)
    {
        this.door = door;
    }
}
